int	ft_atoi(char *str)
{
	int	i;
	int	r;
	int	neg;

	i = 0;
	r = 0;
	neg = 1;
	while (str[i] == '\t'
		|| str[i] == '\v' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\f'
		|| str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - 48);
		i++;
	}
	return (neg * r);
}

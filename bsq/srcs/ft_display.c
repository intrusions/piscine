#include "bsq.h"

void	ft_display(char **map)
{
	int	j;

	j = 0;
	while (map[j] != 0)
	{
		ft_putstr(map[j]);
		ft_putstr("\n");
		free(map[j]);
		j ++;
	}
	free(map);
}
